		  vec2 n_yz = mix(n_z.xy, n_z.zw, fade_xyz.y);
		  float n_xyz = mix(n_yz.x, n_yz.y, fade_xyz.x);
		  return 2.2 * n_xyz;
		}

		float rand(vec2 n)
		{
		  return 0.5 + 0.5 *
		     fract(sin(dot(n.xy, vec2(12.9898, 78.233)))* 43758.5453);
		}

